package org.live.is;

public class Vertice {
	
	private int horizontal;
	private int vertical;
	private int giro;
	
	public int getHorizontal() {
		return horizontal;
	}
	public void setHorizontal(int derecha) {
		this.horizontal = derecha;
	}
	public int getVertical() {
		return vertical;
	}
	public void setVertical(int izquierda) {
		this.vertical = izquierda;
	}
	public int getGiro() {
		return giro;
	}
	public void setGiro(int giro) {
		this.giro = giro;
	}

}
